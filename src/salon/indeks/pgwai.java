    private void ttpegawai(){
       DefaultTableModel tlayanan1 = new DefaultTableModel();
            tlayanan1.addColumn("ID Pengguna Sistem");
            tlayanan1.addColumn("Nama");
            tlayanan1.addColumn("Jenis Kelamin");
            tlayanan1.addColumn("Jabatan");
            tlayanan1.addColumn("tanggal Lahir");
           

            try{
                Connection c = koneksi.getConnection();
                String sql="SELECT * FROM `p_sistem`";
                Statement s = c.createStatement();
                ResultSet rs = s.executeQuery(sql);
                while (rs.next())
                {
                tlayanan1.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                });
                }
                tpegawai.setModel(tlayanan1);
                } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Ada Kesalahan");
                }
    }

