    private void tmbdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tmbdeleteActionPerformed
        // TODO add your handling code here:
        try{
            Connection c = koneksi.getConnection();
            String sql;
            sql = "DELETE FROM `salon`.`p_sistem` WHERE `p_sistem`.`ID_PS` = '"+txtidpegawai.getText()+"';";
            Statement s = c.createStatement();
            int r = s.executeUpdate(sql);
            ttpegawai();
            JOptionPane.showMessageDialog(null,"Data sudah dihapus");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan :"+e.getMessage());
        }
    }//GEN-LAST:event_tmbdeleteActionPerformed

    private void tmbupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tmbupdateActionPerformed
        // TODO add your handling code here:
        String gender = null;
        if(rlaki.isSelected())
        gender ="Laki-laki";
        else if(rperempuan.isSelected())
        gender="Perempuan";
        try{
            Connection c = koneksi.getConnection();
            String sql;
            sql = "UPDATE `salon`.`p_sistem` SET `nama_PS` = '"+txtnmpegawai.getText()+"', `jenis_kel` = '"+gender+"', "
            +   "`jabatan` = '"+cmbjabatan.getSelectedItem()+"', `tgl_lahir` = '"+cmbdate.getSelectedItem()+"' "
            +   "WHERE `p_sistem`.`ID_PS` = '"+txtidpegawai.getText()+"';";
            Statement s = c.createStatement();
            int r = s.executeUpdate(sql);
            ttpegawai();
            JOptionPane.showMessageDialog(null,"Data sudah diubah");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan :"+e.getMessage());
        }
    }//GEN-LAST:event_tmbupdateActionPerformed

